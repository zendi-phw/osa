namespace mod_osa\event;

defined('MOODLE_INTERNAL') || die();

/**
 * Create event class for mod_osa.
 *
 * @package     mod_osa
 * @copyright   2023 Simon Schaudt <schaudt@ph-weingarten.de>
 * @license     https://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class course_module_viewed extends \core\event\base {
    protected function init() {
        $this->data['crud'] = 'c';
        $this->data['edulevel'] = self::LEVEL_OTHER;
        $this->data['objecttable'] = 'osa';
    }

    public static function get_name() {
        return get_string('coursemoduleviewed', 'mod_osa');
    }

    public function get_description() {
        return get_string('coursemodulevieweddesc', 'mod_osa');
    }

    public function get_url() {
        return new \moodle_url('mod_osa/view.php');
    }
}
